# H======================================================================================= Novodo script - runner =====H #

# I=============================================================================================== SYSTEM IMPORTS =====I #

import src.scripts.novUtils as utils

# I================================================================================================ OTHER IMPORTS =====I #

import subprocess
import sys
import os

# I====================================================================================================== IMPORTS =====I #

# M========================================================================================================= MAIN =====M #

def mainRunner(args):
    subprocess.run(['python', os.path.join(utils.SCRIPT_DIR, "src", "main.py")] + args)
    sys.exit(0)

# M========================================================================================================= MAIN =====M #

# R========================================================================================================== RUN =====R #

if __name__ == "__main__":
    try:
        mainRunner(sys.argv[1:])
    except KeyboardInterrupt:
        utils.logging.debug("KeyboardInterrupt Exit")
        sys.exit(0)
    
